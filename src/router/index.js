/**
 * Note: 子菜单仅在路由 children.length > 1 时出现
 *
 * hidden: true                   如果设置为 true，将不会显示在侧边栏中（默认为 false）
 * alwaysShow: true               如果设置为 true，将始终显示根菜单
 *                                如果不设置 alwaysShow，当 item 有多个子路由时，它将变成嵌套模式，否则不显示根菜单

 * redirect: noRedirect           如果设置 noRedirect 将不会在面包屑中重定向
 * name:'router-name'             该名称由 <keep-alive> 使用（必须设置！！！）
 * meta : {
    roles: ['admin','editor']    控制页面角色（可以设置多个角色）
    title: 'title'               名称显示在侧边栏和面包屑中（推荐设置）
    icon: 'svg-name'/'el-icon-x' 图标显示在侧边栏中
    breadcrumb: false            如果设置为 false，项目将隐藏在面包屑中（默认为 true）
    activeMenu: '/example/list'  激活路径样式
  }
 */

